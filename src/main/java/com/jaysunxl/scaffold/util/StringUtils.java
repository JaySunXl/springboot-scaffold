package com.jaysunxl.scaffold.util;

import java.util.Locale;

/**
 * @Description
 * @Author sunxind
 * @Version 1.0.0
 * @Date 2022/12/7
 */
public class StringUtils {

    /**
     * 字符串小写
     *
     * @param str
     * @return
     */
    public static String toLowerCase(String str) {
        return str.toLowerCase(Locale.ROOT);
    }
}
